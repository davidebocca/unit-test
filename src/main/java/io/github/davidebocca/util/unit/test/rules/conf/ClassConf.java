package io.github.davidebocca.util.unit.test.rules.conf;

public class ClassConf {

	private Class<?> clazz;

	public ClassConf(Class<?> clazz) {
		super();
		this.clazz = clazz;
	}

	public Class<?> getClazz() {
		return clazz;
	}

	public void setClazz(Class<?> clazz) {
		this.clazz = clazz;
	}

}
